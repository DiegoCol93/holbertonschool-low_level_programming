#include <unistd.h>
/**
 * _putchar - writes the character c to stdout
 */
int _putchar(char c);

/**
 * print_alphabet - Prints alphabet from a to z
 */
void print_alphabet(void);

/**
 * print_alphabet_x10 - Prints alphabet from a to z ten times
 */
void print_alphabet_x10(void);

/**
 * _islower - Checks if c is an upper or lower case character
 */
int _islower(int c);

/**
 * _isalpha - Chekcs if c is an alphabetic character or not
 */
int _isalpha(int c);

/**
 * print_sign - Prints sign of number n
 */
int print_sign(int n);

/**
 * _abs - Computes the absolute value of a number
 */
int _abs(int);

/**
 * print_last_digit - Prints the last digit of a number
 */
int print_last_digit(int);
