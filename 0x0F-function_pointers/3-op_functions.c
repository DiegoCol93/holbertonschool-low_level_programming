#include "3-calc.h"
/**
 * op_add - Adds a and b.
 * @a: Number a.
 * @b: Number b.
 *
 * Return: Operation's result.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Adds a and b.
 * @a: Number a.
 * @b: Number b.
 *
 * Return: Operation's result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplies a and b.
 * @a: Number a.
 * @b: Number b.
 *
 * Return: Operation's result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides a and b.
 * @a: Number a.
 * @b: Number b.
 *
 * Return: Operation's result.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Operates a modulus b.
 * @a: Number a.
 * @b: Number b.
 *
 * Return: Operation's result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
